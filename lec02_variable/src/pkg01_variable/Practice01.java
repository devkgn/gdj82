package pkg01_variable;

public class Practice01 {

	public static void main(String[] args) {
		/*
		 * 철수는 변수를 배우기 전에 계산식을 출력하는 코드를 작성했습니다.
		 * 변수를 배운 후, 
		 * 변수를 사용하면 여러가지 장점이 있다는 것을 알게 된 철수.
		 * 변수 개념을 도입하여 코드를 수정하고자 합니다.
		 * 철수가 변수를 배우기 전 작성한 코드를 보고, 
		 * 변수 개념을 도입하여 코드를 수정하세요.
		 * 철수가 변수를 배우기 전 작성한 코드는 다음과 같습니다.
		 */
		System.out.println(100 + 10);
		System.out.println(10 * (100 + 10));
		System.out.println((10 * (100 + 10)) / 10);
	}

}
