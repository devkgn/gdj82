package pkg02_type;

public class Practice01 {

	public static void main(String[] args) {
		/*
		 * 로맨틱한 개발자 김철수는 여자친구에게 
		 * 사랑이 넘치는 코드를 쓰려고 합니다.
		 * 아래 지시 사항을 보고 적합한 변수를 선언해주세요.
		 * 초기화한 후 출력까지 해보기!!
		 * 1. 변수명이 love인 논리형 변수 
		 * 2. 너에게 별을 따주고 싶은 마음을 나타내는 char 변수 star 
		 * 3. 너를 향해 가는 길의 길이 byte 변수 street 
		 * 4. 너를 위해 준비한 선물의 개수 short 변수 gift 
		 * 5. 우리가 사랑한 날 int 변수 day 
		 * 6.우리 앞에 펼쳐진 수많은 날들 long 변수 future 
		 * 7. 내 마음속 너와 나의 거리 float 변수 heart 
		 * 8. 너를 사랑하지 않는 마음의 크기 double 변수 hate
		 */
		boolean love;
		love = false;
		System.out.println("love 출력 : "+love);
		char star;
		star = 'A';
		System.out.println("star 출력 : "+star);
		byte street;
		street = 1;
		System.out.println("street 출력 : "+street);
		short gift;
		gift = 23;
		System.out.println("gift 출력 : "+gift);
		int day;
		day = 1000;
		System.out.println("day 출력 : "+day);
		long future;
		future = 222;
		System.out.println("future 출력 : "+future);
		float heart;
		heart = 2.3f;
		System.out.println(heart);
		double hate;
		hate = 0.0000001;
		System.out.println(hate);
		

	}

}
