package pkg01_trycatch.practice;

public class Practice {
	// 두개의 int를 매개변수로 
	// 전달 받는 메소드 3개
	// return이 int
	// - add : 두 수 더하기
	// - multiple : 두 수 곱하기
	// - divide : 두 수 나누기 몫
	
	public int add(int a, int b) {
		return a+b;
	}
	
	public int multiple(int a, int b) {
		return a*b;
	}
	
	public int divide(int a, int b) {
		return a/b;
	}
}
